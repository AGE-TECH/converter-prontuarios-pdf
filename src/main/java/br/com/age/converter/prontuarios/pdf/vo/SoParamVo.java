package br.com.age.converter.prontuarios.pdf.vo;

public class SoParamVo {

	private String urlCodigoBarra;
	private String urlCodigoBarraInterno;
	private String endereco;
	private String javamenu;
	
	public String getUrlCodigoBarra() {
		return urlCodigoBarra;
	}
	
	public void setUrlCodigoBarra(String urlCodigoBarra) {
		this.urlCodigoBarra = urlCodigoBarra;
	}
	
	public String getUrlCodigoBarraInterno() {
		return urlCodigoBarraInterno;
	}
	
	public void setUrlCodigoBarraInterno(String urlCodigoBarraInterno) {
		this.urlCodigoBarraInterno = urlCodigoBarraInterno;
	}

	public String getEndereco() {
		return endereco;
	}

	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}

	public String getJavamenu() {
		return javamenu;
	}

	public void setJavamenu(String javamenu) {
		this.javamenu = javamenu;
	}
	
}
